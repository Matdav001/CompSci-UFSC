# Crescent and decrescent
# 30 of March of 2026

# While loop
while True:
    # Variable declaration
    x = int(input("Digite x: "))
    y = int(input("Digite y: "))

    # If logic and break
    if x == y:
        break

    # If logic and print result
    elif x > y:
        print("Decrescente")
    else:
        print("Crescente")
